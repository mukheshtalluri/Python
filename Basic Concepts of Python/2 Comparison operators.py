"""
Comparison operators : When we want to compare two values we will use comparison operations.
There are few comparison operators:
1. == --> equal
2. != --> not equal
3. < --> less than
4. > --> greater than
5. >= --> greater than or equal
6. <= --> less than or equal
Note : = is an assignment operator not comparison operator.
"""
num_1 = 5
num_2 = 10

print(num_1 == num_2)

print(num_1 != num_2)

print(num_1 < num_2)

print(num_1 > num_2)

print(num_1 <= num_2)

print(num_1 >= num_2)

# Chained comparison operators : we can use the multiple comparison in single statement.

print(2 < 5 > 4)

print(2 == 2 and 3 < 4)

print(2 != 2 or 5 < 6)



